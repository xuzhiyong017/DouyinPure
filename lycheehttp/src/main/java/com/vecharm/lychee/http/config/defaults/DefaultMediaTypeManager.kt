    override fun getTypeBySuffix(suffix: String): String? {
        val types = arrayListOf<String>()
        this.types.forEach {
            if(it.key.equals(suffix,true)){
                types.add(it.value)
            }
        }
        return filter(suffix, types)
    }

    open fun filter(suffix:String,types:ArrayList<String>?):String? = types?.firstOrNull()


